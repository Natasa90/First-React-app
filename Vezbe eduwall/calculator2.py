number1 = int(input("Enter first operand: "))
number2 = int(input("Enter second operand: "))
operation = input("Choose operation(add, sub, mul, div): ")
if(operation == "add"):
    print("Your result is: ", number1 + number2)
elif(operation == "sub"):
    print ("Your result is: ", number1 - number2)
elif(operation == "mul"):
    print ("Your result is: ", number1 * number2)
elif(operation == "div"):
    print ("Your result is: ", number1 / number2)           
         